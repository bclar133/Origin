function buildData(matches) {
  const teams = new Map();
  const careers = new Map();

  for (const match of matches) {
    for (const side of ["left", "right"]) {
      const state = side === "left" ? match.leftState : match.rightState;
      const score = match.scores[state] || 0;
      const otherState = side === "left" ? match.rightState : match.leftState;
      const otherScore = match.scores[otherState] || 0;
      const teamKey = `${match.year}-${state}`;
      const team = ensureTeam(teams, teamKey, state, match.year);
      team.matches += 1;
      team.pointsFor += score;
      team.pointsAgainst += otherScore;
      if (score > otherScore) team.wins += 1;
      if (score < otherScore) team.losses += 1;

      for (const appearance of match.teams[side]) {
        const row = ensurePlayerSeason(team, appearance.player);
        const career = ensureCareer(careers, appearance.player, state);
        addAppearance(row, appearance, match.id);
        addAppearance(career, appearance, match.id);
      }
    }

    for (const score of match.scorers) {
      const teamKey = `${match.year}-${score.state}`;
      const team = teams.get(teamKey);
      if (!team) continue;
      const row = ensurePlayerSeason(team, score.player);
      const career = ensureCareer(careers, score.player, score.state);
      addScore(row, score);
      addScore(career, score);
    }
  }

  for (const team of teams.values()) {
    for (const row of team.players.values()) {
      const career = careers.get(row.id);
      if (!row.positionCounts.size && career?.positionCounts.size) {
        for (const [position, count] of career.positionCounts) addCount(row.positionCounts, position, count * 0.6);
      }
      if (!row.positionCounts.size && row.fallbackPositionCounts.size) {
        for (const [position, count] of row.fallbackPositionCounts) addCount(row.positionCounts, position, count * 0.5);
      }
      if (!row.positionCounts.size && career?.fallbackPositionCounts.size) {
        for (const [position, count] of career.fallbackPositionCounts) addCount(row.positionCounts, position, count * 0.35);
      }
    }
  }

  const outputTeams = [...teams.values()]
    .sort((a, b) => a.year - b.year || a.state.localeCompare(b.state))
    .map((team) => ({
      state: team.state,
      year: team.year,
      players: [...team.players.values()]
        .filter((row) => row.positionCounts.size || row.fallbackPositionCounts.size)
        .map((row) => makePlayer(row, careers.get(row.id), team))
        .sort((a, b) => b.ratings.overall - a.ratings.overall || a.name.localeCompare(b.name))
    }))
    .filter((team) => team.players.length);

  return {
    source: "Rugby League Project State of Origin match pages",
    builtAt: new Date().toISOString(),
    teams: outputTeams,
    immortalWildcards: IMMORTAL_WILDCARDS
  };
}

function makePlayer(row, career, team) {
  const positions = sortedPositions(row.positionCounts);
  const mainPosition = positions[0] || sortedPositions(row.fallbackPositionCounts)[0] || "middle";
  return player(row.name, positions.length ? positions : [mainPosition], makeRatings(row, career, team, mainPosition), makeRole(row, career, mainPosition));
}

function makeRatings(row, career, team, mainPosition) {
  const apps = row.matchIds.size || row.apps || 1;
  const starts = row.starts;
  const careerApps = career?.matchIds.size || apps;
  const careerStarts = career?.starts || starts;
  const scorePoints = row.tries * 4 + row.goals * 2 + row.fieldGoals;
  const careerPoints = (career?.tries || 0) * 4 + (career?.goals || 0) * 2 + (career?.fieldGoals || 0);
  const teamDiff = team.pointsFor - team.pointsAgainst;
  const teamBoost = clamp((team.wins - team.losses) * 1.1 + teamDiff / 42, -3, 4);
  const startBoost = starts * 1.4 + Math.max(0, starts - 1) * 0.25;
  const careerBoost = Math.sqrt(careerApps) * 1.85 + Math.min(7, careerStarts * 0.15);
  const pointsBoost = Math.sqrt(scorePoints) * 0.75 + Math.sqrt(careerPoints) * 0.32;
  const nameBoost = NAME_BONUS.get(row.name) || 0;
  const positionBonus = ((POSITION_WEIGHT[mainPosition] || 1) - 1) * 6;
  const originPeak = 64 + apps * 2.35 + startBoost + careerBoost + pointsBoost + teamBoost + nameBoost + positionBonus;
  const overall = clamp(Math.round(originPeak), 58, 99);
  const spine = ["half", "hooker", "fullback"].includes(mainPosition);
  const outside = ["fullback", "wing", "centre"].includes(mainPosition);
  const forward = ["middle", "edge", "lock"].includes(mainPosition);
  const playmaker = mainPosition === "half";
  const hooker = mainPosition === "hooker";
  const goalAttempts = row.goalAttempts || career?.goalAttempts || 0;
  const goalPct = goalAttempts ? (row.goals || career?.goals || 0) / goalAttempts : 0;

  const attack = clamp(Math.round(overall + (outside ? 4 : 0) + (playmaker ? 3 : 0) + Math.sqrt(row.tries + 1) * 1.8 + (forward ? -3 : 0)), 45, 99);
  const defence = clamp(Math.round(overall + (forward ? 4 : 0) + (hooker ? 3 : 0) - (outside ? 2 : 0)), 45, 99);
  const workrate = clamp(Math.round(overall + (forward ? 5 : 0) + (hooker ? 4 : 0) + Math.min(4, careerApps / 8)), 45, 99);
  const kicking = clamp(Math.round((playmaker ? overall + 6 : spine ? overall - 10 : 24) + Math.min(8, (career?.fieldGoals || 0) * 2.8)), 10, 99);
  const goalKicking = goalAttempts
    ? clamp(Math.round(54 + Math.min(22, goalAttempts * 1.3) + goalPct * 28 + Math.min(8, (career?.goals || 0) / 10)), 35, 99)
    : clamp(Math.round(playmaker ? overall - 12 : outside ? 22 : 12), 10, 55);
  const bigGame = clamp(Math.round(overall + Math.min(8, careerApps / 4) + nameBoost * 0.45 + (apps === 3 ? 1 : 0)), 55, 99);

  return ratings(overall, attack, defence, workrate, kicking, goalKicking, bigGame);
}

function makeRole(row, career, mainPosition) {
  const role = ROLE_BY_POSITION[mainPosition] || "Origin player";
  const apps = career?.matchIds.size || row.matchIds.size || row.apps || 1;
  if (NAME_BONUS.has(row.name)) return `Origin ${role}`;
  if (apps >= 20) return `veteran ${role}`;
  if (apps >= 10) return `regular ${role}`;
  return role;
}

function ensureTeam(teams, key, state, year) {
  if (!teams.has(key)) {
    teams.set(key, {
      state,
      year,
      players: new Map(),
      matches: 0,
      wins: 0,
      losses: 0,
      pointsFor: 0,
      pointsAgainst: 0
    });
  }
  return teams.get(key);
}

function ensurePlayerSeason(team, p) {
  if (!team.players.has(p.id)) team.players.set(p.id, emptyPlayer(p));
  return team.players.get(p.id);
}

function ensureCareer(careers, p, state) {
  if (!careers.has(p.id)) careers.set(p.id, { ...emptyPlayer(p), state });
  return careers.get(p.id);
}

function emptyPlayer(p) {
  return {
    id: p.id,
    name: p.name,
    apps: 0,
    starts: 0,
    matchIds: new Set(),
    positionCounts: new Map(),
    fallbackPositionCounts: new Map(),
    tries: 0,
    goals: 0,
    goalAttempts: 0,
    fieldGoals: 0
  };
}

function addAppearance(target, appearance, matchId) {
  if (!target.matchIds.has(matchId)) {
    target.matchIds.add(matchId);
    target.apps += 1;
  }
  if (appearance.started) target.starts += 1;
  if (appearance.position) {
    addCount(appearance.started ? target.positionCounts : target.fallbackPositionCounts, appearance.position, appearance.started ? 1 : 0.45);
  }
}

function addScore(target, score) {
  target.tries += score.tries || 0;
  target.goals += score.goals || 0;
  target.goalAttempts += score.goalAttempts || 0;
  target.fieldGoals += score.fieldGoals || 0;
}

function sortedPositions(counts) {
  return [...counts.entries()]
    .filter(([, count]) => count > 0)
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .map(([position]) => position)
    .slice(0, 4);
}

function parseMatch(html, id) {
  if (!/State Of Origin/i.test(html)) return null;
  const title = cleanText(matchOne(html, /<h1[^>]*>([\s\S]*?)<\/h1>/i) || matchOne(html, /<title>([\s\S]*?)<\/title>/i));
  const gameTitle = cleanText(matchOne(html, /<h2[^>]*>([\s\S]*?)<\/h2>/i));
  const year = Number(matchOne(title, /\b(19\d{2}|20\d{2})\b/));
  const game = Number(matchOne(gameTitle, /Game\s+(\d+)/i)) || 0;
  if (!year) return null;

  const teamSection = parseSection(html, "match_teams");
  const scoreSection = parseSection(html, "match_scoresheet");
  if (!teamSection || !scoreSection) return null;

  const teams = parseTeamRows(teamSection.body);
  const scorers = parseScoreRows(scoreSection.body, scoreSection.leftState, scoreSection.rightState);
  const scores = {
    [scoreSection.leftState]: 0,
    [scoreSection.rightState]: 0
  };

  for (const score of scorers) {
    scores[score.state] += score.tries * 4 + score.goals * 2 + score.fieldGoals;
  }

  return {
    id,
    year,
    game,
    leftState: teamSection.leftState,
    rightState: teamSection.rightState,
    teams,
    scorers,
    scores
  };
}

function parseSection(html, sectionId) {
  const re = new RegExp(`<tr>\\s*<th[^>]*>\\s*(NSW|QLD)\\s*<\\/th>\\s*<th[^>]*data-section="${sectionId}"[^>]*>[\\s\\S]*?<\\/th>\\s*<th[^>]*>\\s*(NSW|QLD)\\s*<\\/th>\\s*<\\/tr>\\s*<tbody id="${sectionId}">([\\s\\S]*?)<\\/tbody>`, "i");
  const m = html.match(re);
  if (!m) return null;
  return {
    leftState: m[1],
    rightState: m[2],
    body: m[3]
  };
}

function parseTeamRows(body) {
  const teams = { left: [], right: [] };
  for (const row of rows(body)) {
    const cells = cellsFromRow(row);
    if (cells.length < 5) continue;
    const position = parsePosition(cells[2].html);
    const left = parsePlayerCell(cells[0].html);
    const right = parsePlayerCell(cells[4].html);
    const leftNo = numberFromText(cells[1].text);
    const rightNo = numberFromText(cells[3].text);
    const leftPosition = position === "bench" ? positionFromJersey(leftNo) : position;
    const rightPosition = position === "bench" ? positionFromJersey(rightNo) : position;

    if (left) {
      teams.left.push({
        player: left,
        jersey: leftNo,
        position: leftPosition,
        started: position !== "bench" && Boolean(leftPosition)
      });
    }
    if (right) {
      teams.right.push({
        player: right,
        jersey: rightNo,
        position: rightPosition,
        started: position !== "bench" && Boolean(rightPosition)
      });
    }
  }
  return teams;
}

function parseScoreRows(body, leftState, rightState) {
  const scorers = [];
  let currentKind = "";
  for (const row of rows(body)) {
    const cells = cellsFromRow(row);
    if (cells.length < 5) continue;
    const kind = parseScoreKind(cells[2].html);
    if (kind) currentKind = kind;
    if (!currentKind) continue;

    const left = parsePlayerCell(cells[0].html);
    const right = parsePlayerCell(cells[4].html);
    if (left) scorers.push(scoreEntry(left, leftState, currentKind, cells[1].text));
    if (right) scorers.push(scoreEntry(right, rightState, currentKind, cells[3].text));
  }
  return scorers.filter(Boolean);
}

function scoreEntry(p, state, kind, countText) {
  if (kind === "T") {
    return { player: p, state, tries: numberFromText(countText) || 1, goals: 0, goalAttempts: 0, fieldGoals: 0 };
  }
  if (kind === "G") {
    const goal = parseGoalText(countText);
    return { player: p, state, tries: 0, goals: goal.goals, goalAttempts: goal.attempts, fieldGoals: 0 };
  }
  if (kind === "FG") {
    return { player: p, state, tries: 0, goals: 0, goalAttempts: 0, fieldGoals: numberFromText(countText) || 1 };
  }
  return null;
}

function parseGoalText(text) {
  const clean = cleanText(text);
  const fraction = clean.match(/(\d+)\s*\/\s*(\d+)/);
  if (fraction) return { goals: Number(fraction[1]), attempts: Number(fraction[2]) };
  const goals = numberFromText(clean) || 0;
  return { goals, attempts: goals };
}

function parseScoreKind(html) {
  const text = cleanText(html).replace(/\u00a0/g, " ").trim().toUpperCase();
  if (text === "T" || /TRIES/.test(text)) return "T";
  if (text === "G" || /GOALS/.test(text)) return "G";
  if (text === "FG" || /FIELD GOALS?/.test(text)) return "FG";
  return "";
}

function parsePosition(html) {
  const abbr = html.match(/<abbr[^>]*title="([^"]*)"[^>]*>([\s\S]*?)<\/abbr>/i);
  const code = cleanText(abbr?.[2] || html).toUpperCase();
  const title = cleanText(abbr?.[1] || html).toLowerCase();
  const joined = `${code} ${title}`;
  if (/\bFB\b|FULLBACK/.test(joined)) return "fullback";
  if (/\bW\b|WING/.test(joined)) return "wing";
  if (/\bC\b|CENTRE|CENTER/.test(joined)) return "centre";
  if (/\bFE\b|\bHB\b|FIVE|HALF|STAND/.test(joined)) return "half";
  if (/\bHK\b|\bH\b|HOOKER/.test(joined)) return "hooker";
  if (/\bFR\b|\bPR\b|PROP|FRONT/.test(joined)) return "middle";
  if (/\b2R\b|\bSR\b|SECOND|ROW/.test(joined)) return "edge";
  if (/\bL\b|\bLK\b|LOCK/.test(joined)) return "lock";
  if (/\bB\b|BENCH|INTERCHANGE|RESERVE/.test(joined)) return "bench";
  return "";
}

function positionFromJersey(number) {
  if (number === 1) return "fullback";
  if (number === 2 || number === 5) return "wing";
  if (number === 3 || number === 4) return "centre";
  if (number === 6 || number === 7) return "half";
  if (number === 8 || number === 10) return "middle";
  if (number === 9) return "hooker";
  if (number === 11 || number === 12) return "edge";
  if (number === 13) return "lock";
  return "";
}

function rows(html) {
  return [...html.matchAll(/<tr\b[^>]*>[\s\S]*?<\/tr>/gi)].map((m) => m[0]);
}

function cellsFromRow(row) {
  return [...row.matchAll(/<(td|th)\b[^>]*>([\s\S]*?)<\/\1>/gi)].map((m) => ({
    html: m[2],
    text: cleanText(m[2])
  }));
}

function parsePlayerCell(html) {
  const m = html.match(/href="\/players\/(\d+)"[^>]*>([\s\S]*?)<\/a>/i);
  if (!m) return null;
  return {
    id: m[1],
    name: normaliseName(cleanText(m[2]))
  };
}

function extractMatchIds(html) {
  return [...new Set([...html.matchAll(/href="\/matches\/(\d+)"/gi)].map((m) => m[1]))];
}

async function cachedFetch(cacheName, url) {
  const file = path.join(CACHE_DIR, cacheName);
  try {
    cachedFetch.lastFromCache = true;
    return await readFile(file, "utf8");
  } catch {
    cachedFetch.lastFromCache = false;
    let lastStatus = 0;
    for (let attempt = 0; attempt < 5; attempt += 1) {
      const response = await fetch(url, { headers: { "user-agent": USER_AGENT } });
      lastStatus = response.status;
      if (response.ok) {
        const html = await response.text();
        await writeFile(file, html, "utf8");
        return html;
      }
      if (response.status !== 429 && response.status < 500) break;
      const wait = response.status === 429 ? 8000 + attempt * 9000 : 2500 + attempt * 3500;
      console.log(`Waiting ${Math.round(wait / 1000)}s after HTTP ${response.status} for ${url}`);
      await sleep(wait);
    }
    throw new Error(`Fetch failed ${lastStatus}: ${url}`);
  }
}

function ratings(overall, attack, defence, workrate, kicking, goalKicking, bigGame) {
  return { overall, attack, defence, workrate, kicking, goalKicking, bigGame };
}

function player(name, positions, playerRatings, role = "") {
  return { name, positions, ratings: playerRatings, role };
}

function immortal(name, positions, playerRatings, originState) {
  return { ...player(name, positions, playerRatings, "Immortal wildcard"), originState };
}

function cleanText(html = "") {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, "")
    .replace(/<style[\s\S]*?<\/style>/gi, "")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&ndash;/g, "-")
    .replace(/&amp;/g, "&")
    .replace(/\s+/g, " ")
    .trim();
}

function normaliseName(name) {
  return name
    .split(/\s+/)
    .map((part) => part.split("-").map(titleNamePart).join("-"))
    .join(" ")
    .replace(/\bMc([a-z])/g, (_, letter) => `Mc${letter.toUpperCase()}`)
    .replace(/\bO'([a-z])/g, (_, letter) => `O'${letter.toUpperCase()}`);
}

function titleNamePart(part) {
  if (/^[A-Z]{2,3}$/.test(part)) return part;
  if (/^(de|da|van|von)$/i.test(part)) return part.toLowerCase();
  return part.charAt(0).toUpperCase() + part.slice(1).toLowerCase();
}

function numberFromText(text) {
  const m = cleanText(text).match(/\d+/);
  return m ? Number(m[0]) : 0;
}

function matchOne(text, re) {
  return text.match(re)?.[1] || "";
}

function addCount(map, key, amount = 1) {
  if (!key) return;
  map.set(key, (map.get(key) || 0) + amount);
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function readArg(name) {
  const found = args.find((arg) => arg.startsWith(`${name}=`));
  return found ? found.split("=").slice(1).join("=") : "";
}

function countPlayers(teams) {
  return teams.reduce((sum, team) => sum + team.players.length, 0);
}
