import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const DATA_FILE = path.join(ROOT, "origin-data.js");
const HTML_FILES = [path.join(ROOT, "index.html"), path.join(ROOT, "rating-calibration.html")];
const EXPORT_FILE = process.argv[2]
  ? path.resolve(process.argv[2])
  : path.join(ROOT, "outputs", "origin-calibration-export-latest.json");

const RATING_KEYS = ["overall", "attack", "defence", "workrate", "kicking", "goalKicking", "bigGame"];
const POSITION_ORDER = ["fullback", "wing", "centre", "half", "edge", "middle", "lock", "hooker"];

const data = parseData(await readFile(DATA_FILE, "utf8"));
const payload = JSON.parse(await readFile(EXPORT_FILE, "utf8"));

const stats = {
  ratingRows: 0,
  positionCareers: 0,
  ratingPlayersUpdated: 0,
  positionPlayersUpdated: 0,
  missingRatings: [],
  missingCareers: []
};

for (const override of Object.values(payload.seasons || {})) {
  if (!override?.seasonKey || !override.ratings) continue;
  stats.ratingRows += 1;
  const player = findSeasonPlayer(data, override);
  if (!player) {
    stats.missingRatings.push(override.seasonKey);
    continue;
  }
  player.ratings = {
    ...player.ratings,
    ...Object.fromEntries(RATING_KEYS.map((key) => [key, ratingValue(override.ratings[key], player.ratings[key])]))
  };
  stats.ratingPlayersUpdated += 1;
}

for (const override of Object.values(payload.careers || {})) {
  if (!override?.careerId) continue;
  stats.positionCareers += 1;
  const primary = sortPositions(override.positions || []);
  const cover = sortPositions((override.coverPositions || []).filter((position) => !primary.includes(position)));
  let updates = 0;

  for (const team of data.teams || []) {
    for (const player of team.players || []) {
      if (slug(player.name) !== override.careerId) continue;
      player.positions = primary.length ? primary : sortPositions(player.positions || []);
      player.coverPositions = cover;
      updates += 1;
    }
  }

  if (updates) {
    stats.positionPlayersUpdated += updates;
  } else {
    stats.missingCareers.push(override.careerId);
  }
}

data.calibratedAt = new Date().toISOString();
data.calibration = {
  source: path.relative(ROOT, EXPORT_FILE).replaceAll("\\", "/"),
  ratingRows: stats.ratingRows,
  positionCareers: stats.positionCareers
};

const output = `// Generated by scripts/build-origin-data.mjs from Rugby League Project State of Origin match pages.\n// Rating and position calibration can be applied with scripts/apply-calibration-overrides.mjs.\nwindow.ORIGIN_INVINCIBLE_DATA = ${JSON.stringify(data, null, 2)};\n`;

await writeFile(DATA_FILE, output, "utf8");
await bumpHtmlAssetVersion(data.calibratedAt);
console.log(JSON.stringify(stats, null, 2));

function parseData(source) {
  const match = source.match(/window\.ORIGIN_INVINCIBLE_DATA\s*=\s*([\s\S]*?);\s*$/);
  if (!match) throw new Error("Could not parse origin-data.js");
  return JSON.parse(match[1]);
}

function findSeasonPlayer(data, override) {
  const team = (data.teams || []).find((item) => item.state === override.state && Number(item.year) === Number(override.year));
  if (!team) return null;
  return (team.players || []).find((player) => slug(player.name) === override.careerId);
}

function ratingValue(value, fallback) {
  return Number.isFinite(Number(value)) ? clamp(Math.round(Number(value)), 1, 100) : fallback;
}

function sortPositions(positions) {
  return [...new Set(positions.filter((position) => POSITION_ORDER.includes(position)))]
    .sort((a, b) => POSITION_ORDER.indexOf(a) - POSITION_ORDER.indexOf(b));
}

function slug(value) {
  return String(value).toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

async function bumpHtmlAssetVersion(timestamp) {
  const version = timestamp.replace(/[-:.TZ]/g, "").slice(0, 14);
  for (const file of HTML_FILES) {
    let html = await readFile(file, "utf8");
    html = html.replace(/(src="(?:origin-data|app|rating-calibration-tool)\.js)(?:\?v=[^"]*)?"/g, `$1?v=${version}"`);
    await writeFile(file, html, "utf8");
  }
}
